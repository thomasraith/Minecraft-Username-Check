    private void check_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_check_buttonMouseClicked
        URL url = null;
        BufferedInputStream bufferedInput = null;
        byte[] buffer = new byte[1024];
        check_solution.setForeground(Color.black);
        check_solution.setText("checking...");
        
        try {
            url = new URL("http://www.minecraft.net/haspaid.jsp?user=" + username_textfield.getText());
        } catch (MalformedURLException ex) {
            Logger.getLogger(MUC_GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        try 
        {
            bufferedInput = new BufferedInputStream(url.openStream());
            int bytesRead = 0;
             while ((bytesRead = bufferedInput.read(buffer)) != -1) {
                
                String chunk = new String(buffer, 0, bytesRead);
                System.out.println(chunk);
                if ("true".equals(chunk))
                {
                    check_solution.setForeground(Color.black);
                    check_solution.setText("This Minecraft - account ist valid");
                }
                else
                {
                    check_solution.setForeground(Color.red);
                    check_solution.setText("This Minecraft - account ist not valid");
                }
             }
                bufferedInput.close();
            
        } 
        catch (IOException ex) 
        {
            System.out.println("error " + ex);
        }
    }//GEN-LAST:event_check_buttonMouseClicked

