    private void donate_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_donate_itemActionPerformed
        try 
        {
            String os = System.getProperty("os.name");
            if (os != null && os.startsWith("Windows")) {
                Runtime.getRuntime().exec( "rundll32 url.dll,FileProtocolHandler "
                    + "https://www.paypal.com/cgi-bin/webscr?cmd=_s-xclick&hosted_button_id=F7U7X5YLSC3EC");
            } 
            else {
            Runtime.getRuntime().exec("firefox " + "https://www.paypal.com/cgi-bin/webscr?cmd=_s-xclick&hosted_button_id=F7U7X5YLSC3EC");
            }
        } 
        catch (IOException ioe) {
            System.out.println("error: " + ioe);
        }
    }//GEN-LAST:event_donate_itemActionPerformed

